            <td width="52%" align="left" valign="top"><h3>Opstartgids <br />
            </h3>
              <p><strong>Stap 1:</strong><br />
                Is een categorie nodig? <br />
                Als u bijvoorbeeld meerder vacatures wilt aanbieden, is het aan te raden een categorie te maken die u bijvoorbeeld &quot;Vacatures&quot; noemt. Na het maken van een categorie kunt u inhoud aan deze categorie toevoegen, deze zal weergegeven in deze categorie. Als een formulier geen doel emailadres heeft zal het emailadres van de categorie worden gebruikt. Als u geen emailadres aan de categorie toekend zal het configuratie emailadres worden gebruikt.
                <br />
                <br />
                <strong>Stap 2.:</strong><br />
              1 Of meedere categorieen toepassen(s).<br />
              U kunt een korte omschrijving invoeren in het basis invoervak. Dit is om het sjabloon te herkennen. Het is belangrijk de breedte en hoogte van de formuliertabel op te geven. In de volgende stap dient u formuliervelden toe te voegen. U kunt helpteksten toevoegen aan velden welke zullen worden weergegeven in de frontend. <br />
              <br />
                <strong>Stap 3.</strong><br />
                Aanmaken van formulieren.<br />
                Voeg een titel toe en kies een categorie. Als u geen categorie wilt toekennen, kies dan &quot;Zonder categorie&quot;.<br />
                Na Volgende dient u een sjabloon te kiezen. Als u geen doel emailadres heeft ingevoerd dan wordt het emailadres van de categorie gekozen, anders het configuratie emailadres.
                <br />
                Bij &quot;captcha&quot; kunt u kiezen voor een veiligheidscheck om het spammen door bots te voorkomen. <br />
                Een introtekst wordt alleen weergegeven op de categoriepagina's. <br />
                De hoofdtekst wordt alleen weergegeven op de formulierpagina's<br />
                E-mail introtekst is een hint voor u zelf. Het is alleen zichtbaar in de ontvangen emails.
                <br />
                <br />
                <strong>Stap 4.</strong><br />
                Koppelen.<br />
                Op de opsommingspagina van formulieren in de backend kunt u formulieren en categorieen koppelen aan een menu. U kunt ook &quot;Alle Formulieren&quot; en &quot;Zonder categorie&quot; koppelen aan een menu.</p>

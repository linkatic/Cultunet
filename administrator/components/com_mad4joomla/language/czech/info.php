              <p><strong>1. krok:</strong><br />
                Potřebujete kategorii? <br />
                Pokud např. potřebujete zveřejnit několik nabídek k práci, doporučuje se vytvořit si kategorii &quot;práce&quot;. Ke kategorii můžete přidat obsah, který se zobrazí nahoře na stránce s výpisem kategorií. 
		Pokud nemá formulář cílovou e-mail adresu, použije se místo toho e-mail nastavený u kategorie. Pokud kategorii nenastavíte e-mailovou adresu, použije se hlavní e-email z nastavení. 
                <br />
                <br />
                <strong>2. krok:</strong><br />
              Použití jednoho nebo více témat.<br />
              V základních informacích můžete zadat pár slov jako popis. To slouží pro lepší označení šablony. Je důležité nastavit šířku a výšku sloupců pro tabulku s formulářem. 
	      V dalším kroku je nutné vložit položky formuláře. K položkám je možné přidat nápovědu, která se zobrazí uživateli při najetí myší na pole formuláře. <br />
              <br />
                <strong>3. krok</strong><br />
                Vložení formuláře.<br />
                Nastavte titulek a přiřaďte kategorii. Pokud kategorii nastavovat nechcete, zvolte &quot;Bez kategorie&quot;.<br />
                Jako další krok je potřeba vybrat šablonu. Pokud nevložíte cílovou e-mail adresu, e-maily se odešlou na adresu nastavenou u kategorie. Pokud nemá kategorie nastavenou e-mail adresu, použije se hlavní adresa z nastavení.
                <br />
                Zapněte ochranu &quot;captcha&quot;, pokud chcete zamezit robotům vkládat spam do vašeho formuláře. <br />
                Úvodní text bude zobrazen pouze na stránce s výpisem kategoríí. <br />
                Hlavní text bude zobrazen na stránce s formuláři.<br />
                Úvodní text e-mailu je pomůcka pro vás. Zobrazí se pouze v e-mailu, který vám z formuláře přijde.
